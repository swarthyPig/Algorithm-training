import java.util.*;

//실행시키는 단축키 : ctrl + f11
// 글꼴 크기 변경 : ctrl + (+, -)
// 글자정렬 : ctrl + a -> ctrl + i
// 문장 복사 : ctrl + alt + 방향키
// 문장 위아래 이동 : alt + 방향키
// 전체주석 : ctrl + shift + /
// 전체주석풀기 : ctrl + shift + 달러표시
public class Young {

	public static void main(String[] args) {

		int a = 3, b = 4;
		System.out.println(a+" "+b);

		byte v1 = 1; // 1
		char v2 = 'A'; // 2
		short v3 = 3; // 2
		int v4 = 4; // 4
		long v5 = 5; // 8
		float v6 = 6.0f; // 4
		double v7 = 7.0; // 8
		boolean v8 = true; // 1
		
		System.out.println(v1+" "+v2+" "+v3+" "+v4+" "+v5+" "+v6+" "+v7+" "+v8);
		
//		3 4
//		1 A 3 4 5 6.0 7.0 true
	}
}